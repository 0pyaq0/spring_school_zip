package kr.hs.study.dto;

public class DataBean {
	int kor, eng, math;
	int grsum, gravg;
	String name;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getKor() {
		return kor;
	}
	public void setKor(int kor) {
		this.kor = kor;
	}
	public int getEng() {
		return eng;
	}
	public void setEng(int eng) {
		this.eng = eng;
	}
	public int getMath() {
		return math;
	}
	public void setMath(int math) {
		this.math = math;
	}
	public int getGrsum() {
		return grsum;
	}
	public void setGrsum(int grsum) {
		this.grsum = grsum;
	}
	public int getGravg() {
		return gravg;
	}
	public void setGravg(int gravg) {
		this.gravg = gravg;
	}
	
	
	
}
